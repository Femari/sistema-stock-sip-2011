    private void SiguientePaso(){
        int cuit = ((ComboClienteItem)cboClientes.getSelectedItem()).getCuit();
        this.proceso.IdentificarCliente(Integer.toString(cuit));

        int prioridad = ((ComboPrioridadItem)cboPrioridad.getSelectedItem()).getNivel();
        proceso.SetearPrioridadPedido(prioridad);

        parent.SiguientePaso(nroPaso);
    }
    
    private void CargarComboClientes(){
        List<Cliente> clientes = proceso.getClientes();
        for(Cliente cliente:clientes){
            cboClientes.addItem(new ComboClienteItem(cliente));
        }
    }
    
    private void CargarComboPrioridad(){
        List<Prioridad> prioridades = proceso.getPrioridades();
        for(Prioridad prioridad: prioridades){
            cboPrioridad.addItem(new ComboPrioridadItem(prioridad));
        }
    }
    
