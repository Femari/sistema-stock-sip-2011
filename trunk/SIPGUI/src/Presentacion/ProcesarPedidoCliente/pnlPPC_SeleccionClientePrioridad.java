    class ComboClienteItem{
        private Cliente cliente;
        
        public int getCuit(){
            return cliente.getCuit();
        }
        
        public ComboClienteItem(Cliente cliente){
            this.cliente = cliente;
        }
        
        @Override
        public String toString(){
            return this.cliente.getNombre();
        }
    }

    class ComboPrioridadItem{
        private Prioridad prioridad;
        
        public int getNivel(){
            return prioridad.getNivel();
        }
        
        public ComboPrioridadItem(Prioridad prioridad){
            this.prioridad = prioridad;
        }
        
        @Override
        public String toString(){
            return prioridad.getDescripcion();
        }
    }
